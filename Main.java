public class Main {
    public static void main(String[] args) {

        // Fruits array
        String[] fruits = {"apple", "banana", "cherry"};

        for (int i = 0; i < fruits.length; i++) {
            System.out.println(fruits[i]);
        }

        // Numbers array
        int[] numbers = {1, 2, 3, 4};

        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
}
