public class NumberCheck {
    public static void main(String[] args) {
        int num = 0;

        if (num > 0) {
            System.out.println("this number is positive");
        } else if (num == 0) {
            System.out.println("this number is zero");
        } else {
            System.out.println("this number is negative");
        }
    }
}
